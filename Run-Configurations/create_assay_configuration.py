def build_vrv_configuration(model_names, background=False, name="vrv_config"):
    configuration = []
    for model in model_names:
        pred_config = predator_response_vector.copy()
        prey_config = prey_response_vector.copy()
        pred_config["Model Name"] = model[:-2]
        pred_config["Trial Number"] = int(model[-1])
        prey_config["Model Name"] = model[:-2]
        prey_config["Trial Number"] = int(model[-1])
        if background:
            for i, assay in enumerate(prey_config["Assays"]):
                assay["background"] = "Red"
                prey_config["Assays"][i] = assay
            for i, assay in enumerate(pred_config["Assays"]):
                assay["background"] = "Red"
                pred_config["Assays"][i] = assay
        configuration.append(prey_config.copy())
        configuration.append(pred_config.copy())
    with open(f"{name}.json", 'w') as f:
        json.dump(configuration, f, indent=4)


def build_differential_configuration(model_names, environment, name):
    configuration = []
    for model in model_names:
        for i in range(1, 4):
            nat_config = naturalistic_configuration.copy()

            nat_config["Model Name"] = model[:-2]
            nat_config["Trial Number"] = int(model[-1])
            nat_config["Environment Name"] = environment
            nat_config["Assay Configuration Name"] = f"Behavioural-Data-Free-{i}"
            configuration.append(nat_config)

    with open(f"{name}.json", 'w') as f:
        json.dump(configuration, f, indent=4)


def build_naturalistic_configuration(model_names, naturalistic_config_name, predator_config_name, prey_config_name, filename):
    configuration = []
    for model in model_names:
        nat_config = naturalistic_configuration.copy()
        pred_config = predator_behaviour_configuration.copy()
        prey_config = prey_behaviour_configuration.copy()

        nat_config["Model Name"] = model[:-2]
        nat_config["Trial Number"] = int(model[-1])
        nat_config["Environment Name"] = naturalistic_config_name

        pred_config["Model Name"] = model[:-2]
        pred_config["Trial Number"] = int(model[-1])
        pred_config["Environment Name"] = predator_config_name

        prey_config["Model Name"] = model[:-2]
        prey_config["Trial Number"] = int(model[-1])
        prey_config["Environment Name"] = prey_config_name

        configuration.append(nat_config)
        configuration.append(prey_config)
        configuration.append(pred_config)

    with open(f"{filename}.json", 'w') as f:
        json.dump(configuration, f, indent=4)


