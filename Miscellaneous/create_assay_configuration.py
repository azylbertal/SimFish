def build_naturalistic_configuration(model_names, naturalistic_config_name, predator_config_name, prey_config_name, filename):
    configuration = []
    for model in model_names:
        nat_config = naturalistic_configuration.copy()
        pred_config = predator_behaviour_configuration.copy()
        prey_config = prey_behaviour_configuration.copy()

        nat_config["Model Name"] = model[:-2]
        nat_config["Trial Number"] = int(model[-1])
        nat_config["Environment Name"] = naturalistic_config_name

        pred_config["Model Name"] = model[:-2]
        pred_config["Trial Number"] = int(model[-1])
        pred_config["Environment Name"] = predator_config_name

        prey_config["Model Name"] = model[:-2]
        prey_config["Trial Number"] = int(model[-1])
